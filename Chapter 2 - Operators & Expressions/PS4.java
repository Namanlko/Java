// Question 4. Write the following expression in java program.

/* 
  
(v*v - u*u)/(2*a*s) 
 
*/

public class PS4 {
    public static void main(String[] args) {
        int v = 5;
        int u = 2;
        int a = 1;
        int s = 2;
        int value = (v*v - u*u)/(2*a*s);
        System.out.println("Value is "+value);
    }
}
